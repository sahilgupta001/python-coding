# Are of a right angled triangle is 1/2 * Base * Height

base = input("Enter the length of the base of the triangle ")
height = input("Enter the height of the triangle ")

# Just type converting the base and the heights to float type
base = float(base)
height = float(height)

area = 1 / 2 * base * height

print("Area of the triangle is ", area)


